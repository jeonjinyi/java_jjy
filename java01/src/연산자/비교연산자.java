package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		int x=200;
		int y=100;
		System.out.println(x==y); //같아. 결과값=> false
		System.out.println(x!=y); //같지 않지? 결과값=> true
		System.out.println(x>y); //x가 y보다 크니? 결과값=> true
		System.out.println(x>=y); //x가 y보다 같거나 크니? 결과값=> true
		//비교연산자의 결과값은 boolean이다. (true 또는 false: 논리형)
		
		
		
	}

}
