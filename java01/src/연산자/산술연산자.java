package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		// 한줄삭제: ctrl+d
		// 들여쓰기: tab 키
		// 자동정리: Ctrl+Shift+F
		int x = 5;
		int y = 3;
		System.out.println(x+y);
		
		int z = x+y; //+(계산)는 CPU가 하고 메모리(변수)에 저장함
		System.out.println("두 수의 합은 "+z);
		System.out.println(x+"+"+y+"="+z);
		System.out.println(x+"+"+y+"="+(x+y));
		System.out.println(x+"-"+y+"="+(x-y));
		System.out.println(x+"*"+y+"="+(x*y));
		System.out.println(x+"/"+y+"="+(x/y));
		System.out.println(x+"%"+y+"="+(x%y));
		//복사: Ctrl+Alt+화살표 아래
		//이동: Alt+화살표 방향
		
		
		
		
	}

}
